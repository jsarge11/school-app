export function toWordGrade(item) {
    switch(item) {
        case(0) :
        return 'Kindergarden';
        case(1) :
        return 'First';
        case(2) :
        return 'Second';
        case(3) :
        return 'Third';
        case(4) :
        return 'Fourth';
        case(5) :
        return 'Fifth';
        case(6) :
        return 'Sixth';
        default :
        return "Kindergarden";
    }
}